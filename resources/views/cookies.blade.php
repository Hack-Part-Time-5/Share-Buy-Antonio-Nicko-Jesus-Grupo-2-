            <h3 class="mb-2">¿Qué son las cookies?</h3>
            <p class="mb-5">Las Cookies, y otras técnicas similares, son ficheros de texto, imagen o rutinas que el Servicio al que se accede instala o ejecuta en el equipo o dispositivo (PC, Smartphone, Tablet, etc.) del usuario para registrar la actividad de su navegación a través de las páginas y secciones de los Servicios del Grupo Share&Buy.

                Las Cookies se asocian únicamente a un código (ID) que identifica el dispositivo y/o el navegador que utiliza en cada visita, por lo que no proporcionan referencias que permitan deducir otros datos personales del usuario.</p>
