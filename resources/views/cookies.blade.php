                <li>Cookies de sesión: Estas expiran cuando el usuario cierra el navegador y se borran del ordenador o dispositivo en ese momento.</li>
                <li>Cookies persistentes: Expiran en función de cuando se cumpla el objetivo para el que sirven (por ejemplo, para que el usuario se mantenga identificado en los Servicios del Grupo Share&Buy) o bien cuando se borran manualmente.</li>
