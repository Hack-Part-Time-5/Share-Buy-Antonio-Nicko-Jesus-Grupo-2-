                <li>Cookies propias: Son las que envían al ordenador o dispositivo del usuario, nuestros Servicios y son gestionadas exclusivamente por Grupo Share&Buy.</li>
                <li>Cookies de terceros: Aquellas que son enviadas por entidades ajenas al Grupo Share&Buy y que se activan cuando se accede desde nuestros Servicios a redes sociales, a visualizar videos o al acceder a anuncios publicitarios.</li>


                <li>Categorías de datos: Datos de solicitud (información recopilada cuando interactúas con nosotros de
                    otra manera)</li>
                <li>Datos recogidos: La información que nos compartas cuando interactúas con nosotros a través de
                    formularios de solicitud. También cuando aplicas a alguna oferta de trabajo.</li>
