<h3 class="mb-2 mt-5">¿Cómo crear un anuncio en Share&Buy?</h3>
            <p class="mb-5">Mira a tu alrededor y pregúntate para qué quieres tantas cosas. Si tu respuesta es “para nada”, ha llegado el momento de ponernos manos a la obra y empezar a usar Share&Buy.

                La primera vez que accedas a la aplicación deberás darte de alta con tu cuenta. Puedes usar una red social, ya sea Twitter, Google o Facebook, o utilizar un correo electrónico (email).
                
                <h6>✅ Nueva Cuenta / Registro</h6>
                Este será tu Perfil de usuario. Debes registrarse antes de vender o comprar cualquier cosa en la aplicación. Puedes hacerlo mediante las redes sociales o vía una cuenta de email.
                
                A continuación, tendrás que completar algunos datos en tu perfil y, lo más importante,  indicar tu ubicación, ya que esta es la principal ventaja de Share&Buy, la geolocalización.
                
                <br><br><h6>✅ Perfil</h6>
                Una vez finalizado el registro aparecerá una pantalla o ventana en la que podrás ver la información de tu perfil.
                
                Además, también encontraras información sobre: tus mensajes, los artículos vendidos, las opiniones, tus mensajes favoritos, y los artículos comprados.
                
                
                Es ahora cuando debes completar los pasos más importantes. El primero de ellos: las imágenes. Para subir imágenes de tu producto tienes que presionar en el icono de cámara, las fotografías pueden realizarse desde la misma aplicación.
                
                Aconsejamos que te detengas bien en este paso ya que será lo primero que verán las personas sobre el producto que deseas vender. Pon atención a la calidad de las imágenes que vas a subir y la cantidad.
                
                
                
                <br><br><h6>✅ Lista de artículos</h6>
                La lista de productos de la pantalla te permitirá tomar fotos y agregar descripciones de los artículos que desea vender. Añade todas las fotografías que puedas, un par de ellas generales, otras muy cercanas para que se aprecien los detalles.
                
                Y, no olvides que las imágenes, deben estar bien enfocadas y tener buena iluminación.
                
                Además, en esta pantalla podrás configurar los precios, el título, la descripción y seleccionar la mejor categoría para tu producto. Para ello no olvides rellenar los siguientes apartados:
                
                <br><br><h6>✅ ¿Qué vendes?</h6>
                En este campo deberás escoger título de tu producto. Este no tendrá que ser ni muy largo ni muy corto.
                
                Piensa que es lo primero que leerá el comprador una vez que vea la imagen y las fotografías tomadas en el punto anterior.
                
                
                Además, escoge el título de la misma forma que buscarías tú ese producto si estuvieras deseando comprarlo.
                
                <br><br><h6>✅ Descripción del producto</h6>
                En este apartado tienes que describir tu producto o artículo que deseas vender.
                
                Es conveniente, según el tipo de producto, incluir medidas, pesos y cualquier otra característica técnica relevante que ayude al posible comprador a decidirse.
                
                Es importante que lo hagas describiendo aquellos detalles que consideres más relevantes, pero que tampoco no sea demasiado largo. ¡Ah, no cometas faltas de ortografía!
                
                <br><br><h6>✅ Precio</h6>
                Aquí viene el detalle más relevante para encontrar un comprador, el precio, ya que muchas veces decidirá so un producto le interesa o no.
                
                Te recomendamos que eches un vistazo a los productos similares para ver qué precio tienen y así poder orientarte mejor en cual debes escoger.
                
                <br><br><h6>✅ Categoría</h6>
                El último paso de tu perfil, es escoger una categoría adecuada para tu producto.
                
                La aplicación ofrece la opción de realizar las compras por categorías más comunes, como: Electrónica, Hogar y Jardín, etc.
                
                Esto es importante para que la aplicación de Share&Buy pueda mantener un orden útil para que cualquier persona sea capaz de encontrar lo que quiere comprar.
                
